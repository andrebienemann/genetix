int afreq(char c) {
    switch (c) {
        case 'A':
            return 4;
        case 'C':
            return 2;
        case 'D':
            return 2;
        case 'E':
            return 2;
        case 'F':
            return 2;
        case 'G':
            return 4;
        case 'H':
            return 2;
        case 'I':
            return 3;
        case 'K':
            return 2;
        case 'L':
            return 6;
        case 'M':
            return 1;
        case 'N':
            return 2;
        case 'P':
            return 4;
        case 'Q':
            return 2;
        case 'R':
            return 6;
        case 'S':
            return 6;
        case 'T':
            return 4;
        case 'V':
            return 4;
        case 'W':
            return 1;
        case 'Y':
            return 2;
        default:
            return 0;
    }
}
